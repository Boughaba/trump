public class OrganisationModel {

    private String firstOrganisation;
    private String secondeOrganisation;
    private String relationShip;
    private String source;


    public String getFirstOrganisation() {
        return firstOrganisation;
    }

    public void setFirstOrganisation(String firstOrganisation) {
        this.firstOrganisation = firstOrganisation;
    }

    public String getSecondeOrganisation() {
        return secondeOrganisation;
    }

    public void setSecondeOrganisation(String secondeOrganisation) {
        this.secondeOrganisation = secondeOrganisation;
    }

    public String getRelationShip() {
        return relationShip;
    }

    public void setRelationShip(String relationShip) {
        this.relationShip = relationShip;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }
}
